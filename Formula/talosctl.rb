    sha256 "3f167dad1e9539358856d0f73469d935a9eb89dc44c1cf5b6e03a1d6898d103f"
  elsif OS.mac? && Hardware::CPU.arm?
    url "https://github.com/siderolabs/talos/releases/download/v#{version}/talosctl-darwin-arm64",
      verified: "github.com/siderolabs/talos/"
    sha256 "4e22c3b60e7aa184d01acf489bb44ad4274e8ea92ae71e2abfc57395e112b9bf"
  elsif OS.linux? && Hardware::CPU.intel?
    url "https://github.com/siderolabs/talos/releases/download/v#{version}/talosctl-linux-amd64",
      verified: "github.com/siderolabs/talos/"
    sha256 "b4d7c96ed68fb91f566e6404ff3318aed019dc64efab3caa436557c4d59159c9"
